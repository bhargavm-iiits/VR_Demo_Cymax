export const LANGUAGES = [
    { code: 'en', label: 'English', flag: '🇺🇸' },
    { code: 'hi', label: 'हिन्दी', flag: '🇮🇳' },
    { code: 'es', label: 'Español', flag: '🇪🇸' },
]

export function getSubtitlesForMovie(movieId, langCode = 'en') {
    const movieSubs = SUBTITLE_TRACKS[movieId]
    if (!movieSubs) return []
    return movieSubs[langCode] || movieSubs['en'] || []
}

export function getAvailableLanguages(movieId) {
    const movieSubs = SUBTITLE_TRACKS[movieId]
    if (!movieSubs) return []
    return LANGUAGES.filter(l => movieSubs[l.code])
}

export function getCurrentCue(subtitles, currentTime) {
    return subtitles.find(cue => currentTime >= cue.start && currentTime < cue.end) || null
}
